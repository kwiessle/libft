#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	if (s1[i] == '\0')
		return ((char*)s1);
	while ((s1[i] != '\0') && (s2[j] != '\0'))
	{
		if (s1[i] == s2[j])
		{
			i++;
			j++;
		}
		else if (s1[i] != s2[j])
		{
			i++;
			if (s2[j + 1] != '\0')
				j = 0;
		}
	}
	if (s2[j] == '\0')
		return ((char*)s1 + i - j);
	return (0);
}
